const tabs = document.getElementById('galleryTabs');
const mainImage = document.getElementById('mainImage');
const mainTitle = document.getElementById('mainTitle');
const mainCaption = document.getElementById('mainCaption');
const grid = document.getElementById('galleryGrid');

function renderGallery(id) {
  const data = galleryData[id];
  if (!data) return;

  document.querySelectorAll('.gallery-tabs button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.id === id);
  });

  const first = data.images[0];
  mainImage.src = first.src;
  mainImage.alt = data.name;
  mainTitle.textContent = data.name + ' // ' + data.class;
  mainCaption.textContent = first.title + ' · ' + first.caption + ' · ' + data.creator;

  grid.innerHTML = data.images.map((item, index) => `
    <article class="gallery-card" data-index="${index}">
      <img src="${item.src}" alt="${item.title}">
      <b>${item.title}</b>
      <span>${item.caption}</span>
    </article>
  `).join('');

  grid.querySelectorAll('.gallery-card').forEach(card => {
    card.addEventListener('click', () => {
      const item = data.images[Number(card.dataset.index)];
      mainImage.src = item.src;
      mainTitle.textContent = data.name + ' // ' + item.title;
      mainCaption.textContent = item.caption + ' · ' + data.creator;
      window.scrollTo({ top: document.getElementById('gallery').offsetTop - 10, behavior: 'smooth' });
    });
  });
}

Object.entries(galleryData).forEach(([id, data]) => {
  const btn = document.createElement('button');
  btn.type = 'button';
  btn.dataset.id = id;
  btn.textContent = data.name;
  btn.addEventListener('click', () => renderGallery(id));
  tabs.appendChild(btn);
});

renderGallery('seol_gongchan');
